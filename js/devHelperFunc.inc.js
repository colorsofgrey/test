
// Route zeichnen
function drawRoute() {
    routeStr = '';
    ctx1.beginPath();
    
    for (var i = 0; i < route.length - 1; i++) {

        ctx1.moveTo(route[i][0], route[i][1]);                      
        ctx1.lineTo(route[i + 1][0], route[i + 1][1]);

        ctx1.strokeStyle = '#bb4444';
        ctx1.stroke();
    
        routeStr += route[i][0] + ' - ' + route[i][1] + ' | ';
    }
    routeStr += route[route.length - 1][0] + ' - ' + route[route.length - 1][1] + ' | ';

    ctx1.closePath()
}

// Start-Stop-Button
var stop = false;
function setStopVar() {
    if (stop == false) {
        stop = true;
        document.getElementById('stopButton').innerHTML = 'Run';
    }
    else {
        stop = false;
        document.getElementById('stopButton').innerHTML = 'Stop';
        mainLoop();
    }
}

